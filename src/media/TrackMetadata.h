#pragma once

#include <QImage>
#include <QString>

// =============================================================================
// TrackMetadata.h — 单首歌曲的展示用元数据
// =============================================================================
// 由 TrackMetadataReader 从音频文件读出，导入时写入列表项角色，
// 播放时再由 loadTrack 读回并更新底部播放栏。
// =============================================================================
struct TrackMetadata {
    QString title;         // 曲目标题；读不到时用文件名（不含扩展名）
    QString artist;        // 歌手；读不到时为「未知艺术家」
    QString album;         // 专辑；读不到时为「未知专辑」
    QString durationText;  // 时长文本，格式 "mm:ss"；未知时为 "--:--"
    QImage cover;          // 内嵌封面；可能为空（无封面时播放栏显示 ♪）
};
